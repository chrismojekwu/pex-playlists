//function to create a data object from the array of songs
const averageValues = (list) => {
  //below I collect the running total & all the values of the song attributes
  const size = list.length;

  let acoustic = 0,
    dance = 0,
    energy = 0,
    instru = 0,
    live = 0,
    loud = 0,
    speech = 0,
    tempo = 0;

  let acousticArr = [],
    danceArr = [],
    energyArr = [],
    instruArr = [],
    liveArr = [],
    loudArr = [],
    speechArr = [],
    tempoArr = [];

  for (let i = 0; i < list.length; i++) {
    acoustic += parseFloat(list[i].acoustic);
    acousticArr.push(parseFloat(list[i].acoustic));
    dance += parseFloat(list[i].dance);
    danceArr.push(parseFloat(list[i].dance));
    energy += parseFloat(list[i].energy);
    energyArr.push(parseFloat(list[i].energy));
    instru += parseFloat(list[i].instru);
    instruArr.push(parseFloat(list[i].instru));
    live += parseFloat(list[i].live);
    liveArr.push(parseFloat(list[i].live));
    loud += parseFloat(list[i].loud);
    loudArr.push(parseFloat(list[i].loud));
    speech += parseFloat(list[i].speech);
    speechArr.push(parseFloat(list[i].speech));
    tempo += parseFloat(list[i].tempo);
    tempoArr.push(parseFloat(list[i].tempo));
  }

  //create an output object containing the average and max/min values
  const output = {
    acoustic: {
      avg: (acoustic / size).toFixed(4),
      min: Math.min(...acousticArr),
      max: Math.max(...acousticArr),
    },
    dance: {
      avg: (dance / size).toFixed(3),
      min: Math.min(...danceArr),
      max: Math.max(...danceArr),
    },
    energy: {
      avg: (energy / size).toFixed(3),
      min: Math.min(...energyArr),
      max: Math.max(...energyArr),
    },
    instru: {
      avg: (instru / size).toFixed(4),
      min: Math.min(...instruArr),
      max: Math.max(...instruArr),
    },
    live: {
      avg: (live / size).toFixed(3),
      min: Math.min(...liveArr),
      max: Math.max(...liveArr),
    },
    loud: {
      avg: (loud / size).toFixed(1),
      min: Math.min(...loudArr),
      max: Math.max(...loudArr),
    },
    speech: {
      avg: (speech / size).toFixed(3),
      min: Math.min(...speechArr),
      max: Math.max(...speechArr),
    },
    tempo: {
      avg: (tempo / size).toFixed(2),
      min: Math.min(...tempoArr),
      max: Math.max(...tempoArr),
    },
  };

  //using size to check for & append the full list with a range of min, mid, and max number values for comparison
  if (size > 60) {
    for (const attr in output) {
      let third = (output[attr].max - output[attr].min) / 3;

      //switching the value for -db level in loudness attribute
      if (attr === "loud")
        third = (Math.abs(output[attr].min) - output[attr].max) / 3;

      const range = {
        min: [
          output[attr].min.toFixed(4),
          (output[attr].min + third).toFixed(4),
        ],
        mid: [
          (output[attr].min + third).toFixed(4),
          (output[attr].min + third * 2).toFixed(4),
        ],
        max: [
          (output[attr].min + third * 2).toFixed(4),
          output[attr].max.toFixed(4),
        ],
      };
      output[attr].range = range;
    }
  }

  return output;
};

const returnRange = (object) => {
  const values = {
    acoustic: object.acoustic.range,
    dance: object.dance.range,
    energy: object.energy.range,
    instru: object.instru.range,
    live: object.live.range,
    loud: object.loud.range,
    speech: object.speech.range,
    tempo: object.tempo.range,
  };
  return values;
};

